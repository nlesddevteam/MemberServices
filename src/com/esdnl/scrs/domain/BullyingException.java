package com.esdnl.scrs.domain;

public class BullyingException extends Exception {

	private static final long serialVersionUID = 2342230754913765247L;

	public BullyingException() {

		super();

	}

	public BullyingException(String message, Throwable cause) {

		super(message, cause);

	}

	public BullyingException(String message) {

		super(message);

	}

	public BullyingException(Throwable cause) {

		super(cause);

	}

}
