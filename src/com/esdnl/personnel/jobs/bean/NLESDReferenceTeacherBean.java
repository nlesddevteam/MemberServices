	private String adjustScaleRatings(String currentrating){
		String newrating="-1";
		if(this.referenceScale.equals("3")){
			//the value 1 is the same in both
			if(currentrating.equals("3")){
				newrating="4";
			}else if(currentrating.equals("2")){
				newrating="3";
			}else if(currentrating.equals("1")){
				newrating="1";
			}else if(currentrating.equals("0")){
				newrating="-1";
			}
		}else{
			newrating = currentrating;
		}
		
		return newrating;
	}
